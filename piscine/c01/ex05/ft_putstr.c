#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
}

int	main(void)
{
	char	*string;

	string = "double 00 check";
	ft_putstr(string);
	return (0);
}
